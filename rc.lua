function jarradTasklistUpdate(w, buttons, label, data, objects)
    -- update the widgets, creating them if needed
    w:reset()
    for i, o in ipairs(objects) do
        local cache = data[o]
        local iconBox, windowNameBox, bgBox, m, windowButton
        if cache then
            iconBox = cache.iconBox
            windowNameBox = cache.windowNameBox
            bgBox = cache.bgBox
            windowControlsBox = cache.windowControlsBox
            closeImageBox = cache.closeImageBox
            windowButton = cache.windowButton
        else
            iconBox = wibox.widget.imagebox()
            
            windowNameBox = wibox.widget.textbox()
            windowNameBox:set_align("left")
            windowNameBox:buttons(common.create_buttons(buttons, o))
            
            windowControlsBox = wibox.layout.fixed.horizontal()
            closeImageBox = awful.titlebar.widget.closebutton(o)
            windowControlsBox:add(closeImageBox)
            
            windowButton = wibox.layout.align.horizontal()
            windowButton:set_first(iconBox)
            windowButton:set_second(windowNameBox)
            windowButton:set_third(windowControlsBox)
            
            -- And all of this gets a background
            bgBox = wibox.widget.background()
            bgBox:set_widget(windowButton)

            data[o] = {
                iconBox = iconBox,
                windowNameBox = windowNameBox,
                bgBox = bgBox,
								windowControlsBox = windowControlsBox,
                closeImageBox = closeImageBox,
                windowButton = windowButton
            }
        end

        local text, bg, bg_image, icon = label(o, windowNameBox)
        -- The text might be invalid, so use pcall
        if not pcall(windowNameBox.set_markup, windowNameBox, text) then
            windowNameBox:set_markup("<i>&lt;Invalid text&gt;</i>")
        end
        bgBox:set_bg(bg)
        if type(bg_image) == "function" then
            bg_image = bg_image(windowNameBox,o,m,objects,i)
        end
        iconBox:set_image(icon)
        w:add(bgBox)
   end
end




